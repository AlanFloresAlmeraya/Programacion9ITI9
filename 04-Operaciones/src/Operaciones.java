public class Operaciones {
	/**
	 * 
	 * @param n1 Es el valor del primer numero
	 * @param n2 Es el valor del segundo numero
	 * @return el valor de la suma 
	 */
	public int sumar(int n1, int n2){
		return n1+n2;
	}
	/**
	 * 
	 * @param n1 Es el valor del primer numero
	 * @param n2 Es el valor del segundo numero
	 * @return el valor de la resta
	 */
	public int restar(int n1, int n2){
		return n1-n2;
	}
	/**
	 * 
	 * @param n1 Es el valor del primero numero
	 * @param n2 Es el valor del segundo numero
	 * @return el valor de la multiplicacion
	 */
	public int multiplicar(int n1, int n2){
		return n1*n2;
	}
	/**
	 * 
	 * @param n1 Es el valor del primero numero
	 * @param n2 es el valor del segundo numero
	 * @return el valor de la multiplicacion
	 */
	public int dividir (int n1, int n2){
		return n1/n2;
	}
}
